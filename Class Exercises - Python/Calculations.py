#Calculations
print ("90+12 =", 90+12)
print (float(90+12))
print ("90-12 =", 90-12)
print (float(90-12))
print ("90*12 =", 90*12)
print ("30/3 =", 30/3) #results in float
print ("60//4 =", 60//4) #results in integer

