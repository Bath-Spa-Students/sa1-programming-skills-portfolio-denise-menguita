#Lists
#----------------

#Homogeneous List
#a list where the elements are restricted to a specific type
print("Homogeneous List")
program = ["Bath Spa", "Creative Computing", "Year 1"]
print(program)

int = [18, 19, 20, 21]
print(int)

float = [3.14, 9.25, 6.37, 4.89]
print(float)

#Heterogeneous List
#a list of values where each value has a different types
print("Heterogeneous List")
info = ["Denise Menguita", "Group 3", 19]
print(info)

info = ["Anagha Akhila", 2006, 18.4]
print(info)

#Repetition Operator
num = [1,2,3,4]
num_5x = num * 5
print(num_5x)

names = ["Denise", "Marielle", "Menguita"]
name_2x = names * 2
print(name_2x)